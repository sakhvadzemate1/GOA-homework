# 2) greet ფუნქცია
def greet():
    """მომხმარებელს სთხოვს სახელს და მას მიესალმება."""
    name = input("გთხოვთ, შეიყვანეთ თქვენი სახელი: ")
    print(f"გამარჯობა, {name}! კეთილი იყოს შენი მობრძანება! 😊")

# 3) sandwich_make ფუნქცია
def sandwich_make(bread, *ingredients):
    """
    ამზადებს ბუტერბროდს მომხმარებლის მითითებული ინგრედიენტებით.
    :param bread: პურის ტიპი (მაგ. თეთრი, შავი).
    :param ingredients: ინგრედიენტების სია (მაგ. ყველი, ლორი, სალათის ფურცელი).
    """
    print(f"თქვენი ბუტერბროდი მზადდება {bread} პურით და შემდეგი ინგრედიენტებით:")
    for ingredient in ingredients:
        print(f"- {ingredient}")
    print("გმადლობთ შეკვეთისთვის, თქვენი ბუტერბროდი მზადაა! 🥪")

# ფუნქციების გამოძახება
if __name__ == "__main__":
    greet()  # მომხმარებლისთვის მისალმება
    print("\nახლა კი, მოდით ბუტერბროდი მოვამზადოთ!")
    bread_type = input("პურის ტიპი (თეთრი/შავი): ")
    print("შეიყვანეთ ინგრედიენტები (დაშორეთ მძიმით, მაგალითად: ყველი, ლორი, სალათის ფურცელი):")
    ingredients = input().split(", ")
    sandwich_make(bread_type, *ingredients)
